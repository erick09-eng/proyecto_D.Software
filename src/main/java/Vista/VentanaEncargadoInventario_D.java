    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
    int idProducto;
int codigoProducto;
int precioUnitario;
int cantidadProducto;

try {
    idProducto = Integer.parseInt(txtIDProdcuto.getText());
    codigoProducto = Integer.parseInt(txtCodigoProducto.getText());
    precioUnitario = Integer.parseInt(txtPrecioUnitario.getText());
    cantidadProducto = Integer.parseInt(txtCanitdadProducto.getText());
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(null, "Por favor ingrese valores numÃ©ricos vÃ¡lidos.", "Campos InvÃ¡lidos", JOptionPane.WARNING_MESSAGE);
    return;
}
if (idProducto < 0 || codigoProducto < 0 || precioUnitario < 0 || cantidadProducto < 0) {
    JOptionPane.showMessageDialog(null, "Por favor ingrese valores numÃ©ricos no negativos.", "Campos InvÃ¡lidos", JOptionPane.WARNING_MESSAGE);
    return;
}

String nombreProducto = txtNombreProducto.getText();
String descripcion = txtDescripcion.getText();
String categoria = txtCategoria.getText();

if (nombreProducto.isEmpty() || descripcion.isEmpty() || categoria.isEmpty() || txtIDProdcuto.getText().isEmpty() || txtCodigoProducto.getText().isEmpty() || txtPrecioUnitario.getText().isEmpty() || txtCanitdadProducto.getText().isEmpty()) {
    JOptionPane.showMessageDialog(null, "Por favor complete todos los campos.", "Campos Incompletos", JOptionPane.WARNING_MESSAGE);
    return;
}

Producto producto = new Producto(idProducto, nombreProducto, codigoProducto, descripcion, precioUnitario, cantidadProducto, categoria);

try {
    boolean respuesta = controladorProducto.GuardarProducto(producto);
    if (respuesta) {
        JOptionPane.showMessageDialog(null, "Se agregÃ³ el producto");
        llenarTabla();
    } else {
        JOptionPane.showMessageDialog(null, "Revise el cÃ³digo, no se logrÃ³ agregar");
    }
} catch (CodigoProductoDuplicadoException ex) {
    JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
}
    }//GEN-LAST:event_jButton2ActionPerformed

    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        // TODO add your handling code here:
        int codigo=Integer.parseInt(txtCodigoProducto.getText());
        Producto producto=controladorProducto.BuscarProducto(codigo);
        if(producto!=null){
            txtNombreProducto.setText(producto.getNombreProducto());
            txtPrecioUnitario.setText(String.valueOf(producto.getPrecioUnitario()));
            txtCanitdadProducto.setText(String.valueOf(producto.getCantidadProducto()));
            txtIDProdcuto.setText(String.valueOf(producto.getIdProducto()));
            txtDescripcion.setText(producto.getDescripcion());
            txtCategoria.setText(producto.getCategoria());
        }else{
            JOptionPane.showMessageDialog(null, "No se Encontro un producto con ese"+codigo);
        }
        
    }//GEN-LAST:event_BuscarActionPerformed

